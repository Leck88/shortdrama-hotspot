#!/usr/bin/env python3
"""
题材分类共享模块 (Genre Classification)

消除 fetch_hotspot.py 和 generate_script.py 中的重复定义。
所有题材分类逻辑统一在此维护。
"""

# 题材关键词映射表
# 注意：匹配顺序影响分类优先级，越具体的越靠前
GENRE_MAP = {
    "霸总": ["总裁", "霸总", "首富", "CEO", "豪门", "先生是个狠"],
    "婚恋": ["婚", "妻", "夫", "领证", "闪婚", "新婚", "宠妻", "备孕", "婚约"],
    "甜宠": ["甜", "宠", "恋", "爱", "娇", "吻", "上瘾", "只对她"],
    "逆袭": ["逆袭", "翻身", "崛起", "无敌", "巅峰", "摊牌", "不好惹", "战将"],
    "重生": ["重生", "回到", "穿越", "前世", "八零"],
    "古装": ["皇", "帝", "妃", "宫", "朝", "侯", "将军", "古装", "太子", "主母"],
    "复仇": ["复仇", "报仇", "复仇者", "血债", "恩断"],
    "悬疑": ["谜", "案", "侦探", "真相", "谍", "查案", "秘密"],
    "战神": ["战龙", "战神", "枭雄", "修仙", "化神"],
    "逆袭/翻盘": ["离婚", "出狱", "撤资", "逃出"],
}


def classify_genre(title: str) -> list[str]:
    """
    根据剧名关键词自动分类题材

    一部剧可能同时属于多个题材（如"离婚后闪婚"同时属于婚恋和逆袭/翻盘）。
    未匹配任何关键词的归入"其他"。

    参数：
        title: 剧名（字符串）

    返回：
        题材标签列表，如 ["婚恋", "逆袭/翻盘"]；无匹配返回 ["其他"]
    """
    genres = []
    for genre, keywords in GENRE_MAP.items():
        if any(kw in title for kw in keywords):
            genres.append(genre)
    return genres if genres else ["其他"]
